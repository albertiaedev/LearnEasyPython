'''
A while loop is used to execute a set of statements as long
as a condition is True.
'''

i = 0
while i <= 6:
  print(i)
  i += 1 #remember to set a limit and increment i to reach it,
         #or you'll create an infinite loop

print('---------------')

#this is an infinte loop
i = 0
while i <= 6:
  print(i)
