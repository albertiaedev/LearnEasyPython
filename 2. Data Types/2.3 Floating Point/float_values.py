'''
Floating point values are point/decimal numbers.

With these values you can perform any of the arithmetic operations
that we're going to review in module 3.
'''

print(1.0)
print(10.2)
print(100.4)
print(1000.6)

print('----------------')
print(10.2 + 10.2)
print(10.2 - 10.2)
print(10.2 * 10.2)
print(10.2 / 10.2)

print('----------------')
#you can perform arithmetic operations mixing integers
#and floating numbers
