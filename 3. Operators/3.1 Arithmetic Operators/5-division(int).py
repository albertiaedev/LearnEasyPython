print('DIVISION (//)')

# as you've seen, a division always returns a floating point number
# you can use the // sign for returning an integer number after dividing numbers

print(4 // 2)
print(9 // 3)

print('-----------------------------')

# you can add any quantity of numbers you want

print(10 // 2)
print(10 // 2 // 2)
print(10 // 2 // 2 // 2)
print(10 // 2 // 2 // 2 // 2)

print('-----------------------------')

# and you can name a variable

a = 20 // 3
print(a)

print('-----------------------------')

# then add variables

a = 20 // 5
print(a)
b = 100 // 5
print(b)
print(a // b)
