print('DIVISION (/)')

# you can use the / sign for dividing numbers

print(4 / 2)
print(9 / 3)

print('-----------------------------')

# you can add any quantity of numbers you want

print(10 / 2)
print(10 / 2 / 2)
print(10 / 2 / 2 / 2)
print(10 / 2 / 2 / 2 / 2)

print('-----------------------------')

# and you can name a variable

a = 20 / 3
print(a)

print('-----------------------------')

# then add variables

a = 20 / 3
print(a)
b = 100 / 7
print(b)
print(a / b)
