'''
The operator == is used for comparing two data types that
have exactly the same value
'''

#it returns True if both data types are equal
print(10 == 10)
print(5 == 5)

#it returns False if both data types are different
print(10 == 5)
print(6 == 5)

print('------------------')

#this logic can be applied to variables
a = 1
b = 1
c = 2
d = 2

print(a == b)
print(a == c)
print(b == d)
print(c == d)
