'''
The <= operator is used for indicating that a data type has a
smaller or equal value than the one it's being compared to
'''

#it returns True if the first data type is equal than the second
#one or if it is smaller
print(1 <= 2)
print(2 <= 3)
print(3 <= 6)
print(30 <= 35)

print('---------------------')

#it returns False if the first data type is different than the
#second one or if it is greater
print(4 <= 2)
print(6 <= 3)
print(14 <= 10)
print(50 <= 35)
