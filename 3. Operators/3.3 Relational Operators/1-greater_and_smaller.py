'''
Relational operators are used to compare and establish a relation
between strings. They return True or False.
'''

print(4 > 3) # it returns True if the operator in the left is greater than the operator in the right.
print(2 > 3) # it returns False if the operator in the right is greater than the operator in the left.
print('------------------------------')
print(2 < 3) # it returns True if the operator in the right is smaller than the operator in the left.
print(4 < 3) # it returns False if the operator in the left is smaller than the operator in the right.
