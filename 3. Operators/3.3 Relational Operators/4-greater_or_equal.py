'''
The >= operator is used for indicating that a data type has a
greater or equal value than the one it's being compared to
'''

#it returns True if the first data type is equal than the second
#one or if it is greater
print(2 >= 2)
print(3 >= 3)
print(10 >= 6)
print(50 >= 35)

print('---------------------')

#it returns False if the first data type is different than the
#second one or if it is smaller 
print(1 >= 2)
print(2 >= 3)
print(6 >= 10)
print(30 >= 35)
